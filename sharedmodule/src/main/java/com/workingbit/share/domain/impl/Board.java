package com.workingbit.share.domain.impl;

import com.amazonaws.services.dynamodbv2.datamodeling.*;
import com.workingbit.board.common.DBConstants;
import com.workingbit.share.domain.BaseDomain;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NoArgsConstructor;
import org.apache.commons.lang3.ObjectUtils;

/**
 * Created by Aleksey Popryaduhin on 18:31 09/08/2017.
 */
@DynamoDBTable(tableName = DBConstants.BOARD_TABLE)
@Data
@EqualsAndHashCode(of = {"id"})
@NoArgsConstructor
@AllArgsConstructor
public class Board implements BaseDomain {

  @DynamoDBAutoGeneratedKey
  @DynamoDBHashKey(attributeName = "id")
  private String id;

  @DynamoDBTypeConvertedJson(targetType = BoardContainer.class)
  @DynamoDBAttribute(attributeName = "currentBoard")
  private BoardContainer currentBoard;

  public Board(BoardContainer board) {
    this.currentBoard = board;
  }

  @Override
  public Object clone() throws CloneNotSupportedException {
    return new Board(id, ObjectUtils.clone(currentBoard));
  }
}
